package cute.modules.enums;

import java.awt.Color;

public enum Category 
{
	CLIENT  ("Client"	    , new Color(234, 71, 71 ), 0),
	COMBAT  ("Combat"       , new Color(56, 103, 224), 1),
	PLAYER  ("Player"       , new Color(37, 205, 84 ), 2),
	MISC    ("Miscellaneous", new Color(122, 61, 217), 3),
	MOVEMENT("Movement"     , new Color(217, 49, 103), 4),
	RENDER  ("Render"       , new Color(231, 164, 73), 5),
	BOT     ("Bot"          , new Color(208, 68, 195), 6);

	String _name;
	Color _color;
	int _id;

	Category(String name, Color color, int id) 
	{
		this._name  = name;
		this._color = color;
		this._id    = id;
	}

	public String getName() 
	{
		return this._name;
	}

	public Color getColor() 
	{
		return this._color;
	}
	
	public int getId()
	{
		return this._id;
	}
}




